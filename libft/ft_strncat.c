#include "libft.h"

char	*ft_strncat(char *dest, const char *app, size_t len)
{
	char *p;

	p = (char *)dest;
	while (*p)
		p++;
	while (len > 0 && *app)
	{
		*p++ = *app++;
		len--;
	}
	*p++ = '\0';
	return (dest);
}
