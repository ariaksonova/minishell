#include "libft.h"

char		*ft_strtrim(char const *s)
{
	char	*f;

	if (!s || !(f = ft_strdup(s)))
		return (NULL);
	s = f;
	while (*f)
		f++;
	while (*s == ' ' || *s == '\f' || *s == '\r' ||
			*s == '\v' || *s == '\n' || *s == '\t')
		s++;
	while ((*f == ' ' || *f == '\f' || *f == '\r' ||
			*f == '\v' || *f == '\n' || *f == '\t') || (*f == '\0'))
		f--;
	*++f = '\0';
	return (ft_strdup(s));
}
