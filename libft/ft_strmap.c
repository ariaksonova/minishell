#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char *p;
	char *pp;

	if (s && f)
	{
		if ((p = ft_strnew(ft_strlen(s))))
		{
			pp = p;
			while (*s)
			{
				*p++ = (*f)(*s);
				s++;
			}
			return (pp);
		}
	}
	return (NULL);
}
