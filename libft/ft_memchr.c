#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	unsigned char *p;

	p = (unsigned char*)str;
	while (n--)
		if (*p != (unsigned char)c)
			p++;
		else
			return (p);
	return (0);
}
